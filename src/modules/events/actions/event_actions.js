// --------------------------------------------------------------------------------

function getAllEvents() {

  return {
    type: 'GET_ALL_EVENTS',
    promise: ApiCallLib.get(`/event`, {})
  }
} // <= create


function getOneEvent(eventId) {

  return {
    type: 'GET_ONE_EVENT',
    promise: ApiCallLib.get(`/event/${eventId}`, {})
  }
} // <= create


// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------
// --------------------------------------------------------------------------------


export default {
    getAllEvents,
    getOneEvent
};
